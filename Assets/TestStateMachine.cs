using System.Collections;
using UnityEngine;

public class TestStateMachine : StateMachineBehaviour {

    public bool canFlinch = true;

    // OnStateEnter is called before OnStateEnter is called on any state inside this state machine
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex) {
        if (!canFlinch) {
            return;
        }
        canFlinch = false;
        GameManager.LocalPlayer.StartCoroutine(FlinchDelay(animator, stateInfo, layerIndex));
        return;
    }

    IEnumerator FlinchDelay(Animator animator, AnimatorStateInfo stateInfo, int layerIndex) {
        yield return new WaitForSeconds(2f);
        canFlinch = true;
    }

